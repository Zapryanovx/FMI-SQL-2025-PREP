SELECT LAPTOP.MODEL, LAPTOP.CODE, LAPTOP.SCREEN
FROM LAPTOP
WHERE LAPTOP.SCREEN = 11 

UNION

SELECT LAPTOP.MODEL, LAPTOP.CODE, LAPTOP.SCREEN
FROM LAPTOP
WHERE LAPTOP.SCREEN = 15;

